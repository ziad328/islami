    companion object {
        val CHAPTER_NAME = "chapter name"
        val CHAPTER_POSITION = "chapter position"
    }

    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        return inflater.inflate(R.layout.fragment_quran, container, false)
    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)
        initRecyclerView(view)
    }

    private fun initRecyclerView(view: View) {
        recyclerView = view.findViewById(R.id.recycler_view)
        if (Locale.getDefault().language.contentEquals("en")) {
            chapterNames = readFromFile("surah_names.txt")
        }
        adapter = ChapterNamesAdapter(chapterNames)
        adapter.onChapterClick = { chapterName, position ->
            showChapterDetails(chapterName, position)
        }
        recyclerView.adapter = adapter
    }

    private fun readFromFile(fileName: String): List<String> {
        val fileContent = requireActivity().application.assets.open(fileName).bufferedReader().use {
            it.readText()
        }

        return fileContent.split("\n")
    }


    private fun showChapterDetails(chapterName: String, position: Int) {
        val intent = Intent(requireContext(), ChapterDetailsActivity::class.java)
        intent.putExtra(CHAPTER_NAME, chapterName)
        intent.putExtra(CHAPTER_POSITION, position)
        startActivity(intent)

    }
